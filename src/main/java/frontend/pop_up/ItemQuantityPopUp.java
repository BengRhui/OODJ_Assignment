    private void minusLabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_minusLabelMouseClicked
        
        // Retrieve the current quantity
        int currentQuantity = Integer.parseInt(quantityField.getText());
        
        // Check the current input, take action if the input is more than to 1
        if (Integer.parseInt(quantityField.getText()) > 1) {
            
            // Reduce the quantity by 1
            currentQuantity--;
            
            // Update the field
            quantityField.setText(String.valueOf(currentQuantity));
        }
    }//GEN-LAST:event_minusLabelMouseClicked

    private void plusLabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_plusLabelMouseClicked

        // Retrieve the current quantity
        int currentQuantity = Integer.parseInt(quantityField.getText());
        
        // Check the current input, take action if the input is more than to 1
        if (Integer.parseInt(quantityField.getText()) > 0) {
            
            // Increase the quantity by 1
            currentQuantity++;
            
            // Update the field
            quantityField.setText(String.valueOf(currentQuantity));
        }
    }//GEN-LAST:event_plusLabelMouseClicked

