    private void generateReceiptButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generateReceiptButtonActionPerformed

        // Generate the receipt
        boolean generateReceipt = currentTransaction.generateReceipt();
        
        // Get the status
        if (generateReceipt) {
        
            // Create message to indicate success
            SystemPopUp successMessage = new SystemPopUp(
                    this,
                    "Receipt Downloaded",
                    "The receipt has been downloaded to the Downloads folder.",
                    new String[]{"OK"}
            );
            successMessage.setVisible(true);
            
            // Enable the parent frame
            MainPage.currentFrame.setEnabled(true);
            
            // Dispose the current frame
            dispose();
            
        } else {
        
            // Create message to indicate failure
            SystemPopUp errorMessage = new SystemPopUp(
                    this,
                    "Error",
                    "Error in downloading receipt. Please inspect code.",
                    new String[]{"OK"}
            );
            errorMessage.setVisible(true);
        }
    }//GEN-LAST:event_generateReceiptButtonActionPerformed

